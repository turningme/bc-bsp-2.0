package com.chinamobile.bcbsp.fault.browse;

public class Number {
	private int num;
	private String string;

	Number(int num, String string) {
		this.num = num;
		this.string = string;
	}

	public int getByInt() {
		return this.num;
	}

	public String getByString() {
		return this.string;
	}
}
